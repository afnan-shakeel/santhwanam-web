/**
 * IconComponent - Renders Heroicons Outline SVG icons
 * 
 * Usage:
 * <app-icon name="home" class="size-5" />
 * <app-icon name="settings" [size]="6" />
 */
@Component({
  selector: 'app-icon',
  standalone: true,
  template: `
    <svg 
      [attr.class]="sizeClasses()"
      fill="none" 
      viewBox="0 0 24 24" 
      stroke-width="1.5" 
      stroke="currentColor"
      aria-hidden="true">
      @for (path of paths(); track path) {
        <path stroke-linecap="round" stroke-linejoin="round" [attr.d]="path" />
      }
    </svg>
  `,
  styles: [`
    :host {
      display: inline-flex;
      align-items: center;
      justify-content: center;
    }
  `]
})
export class IconComponent {
  /** Icon name (Heroicons name or Material Symbols alias) */
  name = input.required<string>();
  
  /** Size in Tailwind units (e.g., 5 = w-5 h-5 = 20px) */
  size = input<number>(5);

  sizeClasses = computed(() => {
    const s = this.size();
    return `w-${s} h-${s}`;
  });

  paths = computed(() => {
    const iconName = this.name();
    
    // Check if it's an alias (Material Symbols name)
    const resolvedName = ICON_ALIAS[iconName] || iconName;
    
    // Get path(s) for the icon
    const mainPath = ICON_PATHS[resolvedName];
    const innerPath = ICON_PATHS[resolvedName + '-inner'];
    
    if (!mainPath) {
      // Fallback to circle if icon not found
      console.warn(`Icon not found: ${iconName} (resolved: ${resolvedName})`);
      return [ICON_PATHS['circle']];
    }
    
    return innerPath ? [mainPath, innerPath] : [mainPath];
  });
}
